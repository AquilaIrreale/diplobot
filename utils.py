def make_grid(l):
    r3 = len(l) % 3
    r4 = len(l) % 4

    if r4 == 0:
        cols = 4
    elif r3 == 0:
        cols = 3
    else:
        cols = 3 if r3 > r4 else 4

    return [l[i:i+cols] for i in range(0, len(l), cols)]
